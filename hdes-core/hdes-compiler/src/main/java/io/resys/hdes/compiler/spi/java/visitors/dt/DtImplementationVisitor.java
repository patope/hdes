import io.resys.hdes.ast.api.nodes.AstNode.TypeName;
import io.resys.hdes.ast.api.nodes.DecisionTableNode.DecisionTableBody;
import io.resys.hdes.ast.api.nodes.DecisionTableNode.HitPolicy;
import io.resys.hdes.ast.api.nodes.DecisionTableNode.HitPolicyAll;
import io.resys.hdes.ast.api.nodes.DecisionTableNode.HitPolicyFirst;
import io.resys.hdes.ast.api.nodes.DecisionTableNode.HitPolicyMatrix;
import io.resys.hdes.ast.api.nodes.DecisionTableNode.MatrixRow;
import io.resys.hdes.ast.api.nodes.ImmutableScalarTypeDefNode;
import io.resys.hdes.compiler.spi.NamingContext;
import io.resys.hdes.compiler.spi.java.visitors.JavaSpecUtil;
import io.resys.hdes.compiler.spi.java.visitors.dt.DtJavaSpec.DtMethodSpec;
import io.resys.hdes.compiler.spi.java.visitors.dt.DtJavaSpec.DtMethodsSpec;
import io.resys.hdes.compiler.spi.java.visitors.dt.DtJavaSpec.DtTypesSpec;
import io.resys.hdes.executor.api.HdesExecutable;

public class DtImplementationVisitor extends DtTemplateVisitor<DtJavaSpec, TypeSpec> {

  private final NamingContext naming;
  private DecisionTableBody body;

  public DtImplementationVisitor(NamingContext naming) {
    super();
    this.naming = naming;
  }
  
  @Override
  public TypeSpec visitDecisionTableBody(DecisionTableBody node) {
    this.body = node;
    List<AnnotationSpec> annotations = Arrays.asList(
        AnnotationSpec.builder(javax.annotation.processing.Generated.class)
        .addMember("value", "$S", DtImplementationVisitor.class.getCanonicalName()).build());
    
    TypeSpec result = TypeSpec.interfaceBuilder(naming.dt().api(node))
        .addModifiers(Modifier.PUBLIC)
        .addAnnotations(annotations)
        .addSuperinterface(naming.dt().executable(node))
        .addTypes(visitHeaders(node.getHeaders()).getValues()).build();
    return result;
  }
  
  @Override
  public DtTypesSpec visitHeaders(Headers node) {
    Function<ClassName, TypeSpec.Builder> from = (name) -> {
      ClassName jsonType = JavaSpecUtil.immutable(name);
      return TypeSpec
          .interfaceBuilder(name)
          .addAnnotation(Immutable.class)
          .addAnnotation(AnnotationSpec.builder(ClassName.get("com.fasterxml.jackson.databind.annotation", "JsonSerialize")).addMember("as", "$T.class", jsonType).build())
          .addAnnotation(AnnotationSpec.builder(ClassName.get("com.fasterxml.jackson.databind.annotation", "JsonDeserialize")).addMember("as", "$T.class", jsonType).build())
          .addModifiers(Modifier.PUBLIC, Modifier.STATIC);
    };
        
    TypeSpec.Builder inputBuilder = from.apply(naming.dt().inputValue(body))
        .addSuperinterface(HdesExecutable.InputValue.class);
    
    TypeSpec.Builder outputBuilder = from.apply(naming.dt().outputValueFlux(body))
        .addSuperinterface(HdesExecutable.OutputValue.class);
    
    
    for(TypeDefNode header : node.getValues()) {
      MethodSpec method = visitHeader(header).getValue();
      if(header.getDirection() == DirectionType.IN) {
        inputBuilder.addMethod(method);
      } else {
        outputBuilder.addMethod(method);        
      }
    }
    
    // Outputs generated by hit policy
    outputBuilder.addMethods(visitHitPolicy(body.getHitPolicy()).getValue());
    
    List<TypeSpec> values = new ArrayList<>();
    values.add(inputBuilder.build());
    values.add(outputBuilder.build());
    
    boolean isCollection = body.getHitPolicy() instanceof HitPolicyAll;
    if (isCollection) {
      ParameterizedTypeName returnType = ParameterizedTypeName.get(ClassName.get(Collection.class), naming.dt().outputValueFlux(body));
      TypeSpec collectionOutput = from.apply(naming.dt().outputValueMono(body))
        .addSuperinterface(HdesExecutable.OutputValue.class)
        .addMethod(MethodSpec.methodBuilder(JavaSpecUtil.getMethodName("values"))
          .addModifiers(Modifier.PUBLIC, Modifier.ABSTRACT)
          .returns(returnType)
          .build()).build();
      values.add(collectionOutput);
    }
    
    return ImmutableDtTypesSpec.builder().addAllValues(values).build();
  }

  @Override
  public DtMethodSpec visitHeader(TypeDefNode node) {
    ScalarTypeDefNode scalar = (ScalarTypeDefNode) node;
    MethodSpec method = MethodSpec.methodBuilder(JavaSpecUtil.getMethodName(node.getName()))
        .addModifiers(Modifier.PUBLIC, Modifier.ABSTRACT)
        .returns(JavaSpecUtil.type(scalar.getType()))
        .build();
    return ImmutableDtMethodSpec.builder().value(method).build();
  }

  @Override
  public DtMethodsSpec visitHitPolicyMatrix(HitPolicyMatrix node) {
    List<MethodSpec> value = new ArrayList<>();
    
    for(MatrixRow row : node.getRows()) {
      TypeName headerName = row.getTypeName();
      ScalarTypeDefNode scalar = (ScalarTypeDefNode) body.getHeaders().getValues().stream()
      .filter(t -> t.getName().equals(headerName.getValue()))
      .findFirst().get();
      
      DtMethodSpec method = visitHeader(ImmutableScalarTypeDefNode.builder()
          .from(scalar)
          .type(node.getToType())
          .build());
      
      value.add(method.getValue());
    }
    
    return ImmutableDtMethodsSpec.builder().value(value).build();
  }

  @Override
  public DtMethodsSpec visitHitPolicyAll(HitPolicyAll node) {
    return ImmutableDtMethodsSpec.builder().build();
  }
  
  @Override
  public DtMethodsSpec visitHitPolicyFirst(HitPolicyFirst node) {
    return ImmutableDtMethodsSpec.builder().build();
  }
  
  private DtMethodsSpec visitHitPolicy(HitPolicy node) {
    if (node instanceof HitPolicyAll) {
      return visitHitPolicyAll((HitPolicyAll) node);
    } else if (node instanceof HitPolicyMatrix) {
      return visitHitPolicyMatrix((HitPolicyMatrix) node);
    }
    return visitHitPolicyFirst((HitPolicyFirst) node);
  }
}
